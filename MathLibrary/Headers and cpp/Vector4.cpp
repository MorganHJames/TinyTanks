#include "Vector4.h"


namespace Vector4
{
	double Vector4::Add(double a, double b)
	{
		return a + b;
	}

	double Vector4::Subtract(double a, double b)
	{
		return a - b;
	}

	double Vector4::Multiply(double a, double b)
	{
		return a * b;
	}

	double Vector4::Divide(double a, double b)
	{
		return a / b;
	}
}
