#include "MathUtil.h"


namespace MathUtil
{
	double MathUtil::Add(double a, double b)
	{
		return a + b;
	}

	double MathUtil::Subtract(double a, double b)
	{
		return a - b;
	}

	double MathUtil::Multiply(double a, double b)
	{
		return a * b;
	}

	double MathUtil::Divide(double a, double b)
	{
		return a / b;
	}
}
