#include "Vector3.h"


namespace Vector3
{
	double Vector3::Add(double a, double b)
	{
		return a + b;
	}

	double Vector3::Subtract(double a, double b)
	{
		return a - b;
	}

	double Vector3::Multiply(double a, double b)
	{
		return a * b;
	}

	double Vector3::Divide(double a, double b)
	{
		return a / b;
	}
}
