#ifndef _SPRITE_H_
#define _SPRITE_H_

#include "Node.h"

//\===========================================================================================
//\ Author: Morgan James
//\ Class: Node
//\ Brief: A very simple sprite class that extends from node allowing us to have a sprite hierarchy tree in our game
//\===========================================================================================

class Sprite : public Node 
{
private:
	int m_iSpriteID;
	int m_iWidth;
	int m_iHeight;
	Vector2 m_v2Origin;

public:
	//\===========================================================================================
	//\ Constructor -- Makes A UG Sprite Object 
	//\===========================================================================================
	
	Sprite(const char* a_fileName, float a_width, float a_height, Vector2 a_origin, Vector4 a_UVCoords);

    //\===========================================================================================
    //\ Destructor
    //\===========================================================================================
    
    virtual ~Sprite();
    
    //\===========================================================================================
    //\ Set The Sprite Layer
    //\===========================================================================================
    
    void SetLayer(unsigned int a_uiLayer);
    
    //\===========================================================================================
    //\ Update Sprites Data Each Frame
    //\===========================================================================================
    
    void Update();
    
    //\===========================================================================================
    //\ Move Sprite 
    //\===========================================================================================
    
    void MoveSprite(Vector3 a_movementVec);
    
    //\===========================================================================================
    //\ Mark For Draw
    //\===========================================================================================
    
    void MarkForDraw();
    
    //\===========================================================================================
    //\ Stop Drawing
    //\===========================================================================================
    
    void StopDrawing();
    
    //\===========================================================================================
    //\ Set Position
    //\===========================================================================================
    
    void SetPosition(Vector2 a_pos);
	
};

#endif