#ifndef _SPRITE_H_
#define _SPRITE_H_

#include "Node.h"

//\===========================================================================================
//\ Author: Morgan James
//\ Class: Node
//\ Brief: A very simple sprite class that extends from node allowing us to have a sprite hierarchy tree in our game
//\===========================================================================================

class Sprite : public Node 
{
private:
	int m_iSpriteID;
	
	int m_iWidth;
	
	int m_iHeight;
	
	Vector2 m_v2Origin;

public:
	//\===========================================================================================
	//\ Constructor -- Makes A UG Sprite Object 
	//\===========================================================================================
	
	Sprite(const char* a_c_cFileName, float a_c_fWidth, float a_c_fHeight, Vector2 a_c_v2Origin, Vector4 a_c_v4UVCoords);

    //\===========================================================================================
    //\ Destructor
    //\===========================================================================================
    
    virtual ~Sprite();
    
    //\===========================================================================================
    //\ Set The Sprite Layer
    //\===========================================================================================
    
    void setLayer(unsigned int a_uiLayer);
    
    //\===========================================================================================
    //\ Update Sprites Data Each Frame
    //\===========================================================================================
    
    void update();
    
    //\===========================================================================================
    //\ Move Sprite 
    //\===========================================================================================
    
    void moveSprite(Vector3 a_v3MovementVec);
    
    //\===========================================================================================
    //\ Mark For Draw
    //\===========================================================================================
    
    void markForDraw();
    
    //\===========================================================================================
    //\ Stop Drawing
    //\===========================================================================================
    
    void stopDrawing();
    
    //\===========================================================================================
    //\ Set Position
    //\===========================================================================================
    
    void setPosition(Vector2 a_v2Pos);
	
};

#endif